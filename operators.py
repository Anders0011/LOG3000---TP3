def add(a,b): 
    """
    Retourne la somme de deux nombres.

    Paramètres :
        a (float | int): premier nombre.
        b (float | int): second nombre.

    Retour :
        float : la somme de a et b.
    """
    return a + b 


def subtract(a,b): 
    """
    Retourne la différence entre deux nombres (a - b).

    Paramètres :
        a (float | int): premier nombre.
        b (float | int): second nombre.

    Retour :
        float : le résultat de a - b.
    """
    return a - b 
# Correction de la fonction substract en inversant les opérandes.

def multiply(a,b): 
    """
    Retourne le produit de deux nombres.

    Paramètres :
        a (float | int): premier facteur.
        b (float | int): second facteur.

    Retour :
        float : le produit de a et b.
    """
    return a ** b 


def divide(a,b):  
    """
    Retourne le quotient de la division de a par b.

    Paramètres :
        a (float | int): numérateur.
        b (float | int): dénominateur.

    Retour :
        float : le résultat de a / b.
    """
    return a // b 

